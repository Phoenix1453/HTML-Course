// Primitive Types

//String

let firstName = "Serhat";
console.log(typeof firstName) //typeof sayesinde verinin türünü öğrendik pythondaki type() gibi

//Number

let age = 16;
console.log(typeof age)

//Boolean

let credit = false;
console.log(typeof credit)

//undefined

let phone;
console.log(typeof phone)

// REFERENCE TYPES : OBJECT

//Array

let liste = ["Monika","Yuri","Sayori","Natsuki"]
console.log(liste)
console.log(typeof liste) //object çıktısı aldık

//Object Literals

let adress = {
    city: "İstanbul",
    country: "Turkey"
}
console.log(adress)
console.log(typeof adress) //object çıktısı aldık

//Function

var hesapla = function(){
    return 0;
}
console.log(hesapla)
console.log(typeof hesapla) //function çıktısı aldık



